void main(){
  ///4 types of operators
  ///1. unary prefix operator
  ///2. unary postfix operator
  ///3. binary infix operator
  ///4. compound assignment operator
  var age=10;
  print(--age); //unary prefix operator [storing the value in age variable and then printing it]
  print(++age); //unary postfix operator [storing the value in age variable and then printing it]
  ///unary bitwse complement prefix operator
  print(~1);
}